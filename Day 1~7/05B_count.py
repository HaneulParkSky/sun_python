# for 명령어로 숫자 반복하여 출력하기

print("[0~4]")
for x in range(5):
    print(x)

print("[1~10]")
for x in range(1, 11):
    print(x)


print("[30~50]")
for x in range(30, 51):
    print(x)

print("[100~110]")
for x in range(100, 111):
    print(x)

print("[1~10 홀수]")
for x in range(1,11,2):
    print(x)

print("[1~10 짝수]")
for x in range(2,11,2):
    print(x)
    
    
